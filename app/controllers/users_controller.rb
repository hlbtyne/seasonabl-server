class UsersController < ApplicationController
    def index
        users = User.all
        render json: users, include: [:foods]
    end
    
    def show
        user = User.find_by(id: params[:id])
        if user
            render json: user, include: [:shopping_list_items, :user_recipes]
        else
            render json: {error: "User not found."}, status: 404
        end
    end

    def create 
        newUser = User.new(name: params[:name], password: params[:password])
        
        if newUser.save
            render json: newUser
        else
            render json: {error: "User not valid."}, status: 400
        end
    end 

    def login
        user = User.find_by(name: params[:name])
        if user && user.authenticate(params[:password])
            render json: { name: user.name, token: issue_token({ id: user.id }) }
        else
            render json: { error: "Invalid username/password combination" }, status: 401
        end
    end

